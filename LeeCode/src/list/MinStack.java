package list;

import java.util.Stack;

public class MinStack {

	private Stack<Integer> stack;
	private int min;

	public MinStack() {
		stack = new Stack<>();
		min = Integer.MAX_VALUE;
	}

	public void push(int x) {
		if (x <= min) {
			stack.push(min);
			min = x;
		}

		stack.push(x);
	}

	public void pop() {
		if (min == stack.pop()) {
			min = stack.pop();
		}
	}

	public int top() {
		return stack.peek();
	}

	public int getMin() {
		return min;
	}

}
